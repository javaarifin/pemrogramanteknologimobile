fun main() {
    // Mencetak poin pertama tentang variabel read-only (val)
    println("Use the val keyword when the value doesn't change.")

    // Mencetak poin kedua tentang variabel yang dapat diubah (var)
    println("Use the var keyword when the value can change.")

    // Mencetak poin ketiga tentang definisi parameter dalam fungsi
    println("When you define a function, you define the parameters that can be passed to it.")

    // Mencetak poin keempat tentang pemberian argumen saat pemanggilan fungsi
    println("When you call a function, you pass arguments for the parameters.")
}